/*1.	Escreva um programa que declare uma variável inteira e atribua o valor
10 a mesma. Declare uma variável real e atribua para a mesma o valor 20.3. 
Como saída o programa deverá imprimir, usando as variáveis declaradas, 
o seguinte resultado:
a) O valor inteiro é 10.
b) O valor real é 20.3. 
 */
package ExercíciosLista01;
public class Exercício01 {
    public static void main(String[] args) {
        int a; double b;
        a=10;
        b=20.3;
        System.out.println("Valor inteiro é "+a);
        System.out.println("Valor real é "+b);
    }
}
