/*18. Apresentar todos os valores numéricos inteiros entre 10 e 20. No final 
imprimir a soma dos valores apresentados.*/
package ExercíciosLista03;

public class Exercício18 {
    public static void main(String[] args) {
        int a=0;
        for (int i = 10; i <= 20; i++) {
           a=a+i;
            System.out.println(""+a);
        }
    }
    
}
